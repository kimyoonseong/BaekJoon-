SELECT A.DEPT_ID       AS DEPT_ID, 
       A.DEPT_NAME_EN  AS DEPT_NAME_EN, 
       ROUND(AVG(SAL)) AS AVG_SAL
  FROM HR_DEPARTMENT A,
       HR_EMPLOYEES  B
 WHERE A.DEPT_ID= B.DEPT_ID
 GROUP BY A.DEPT_ID
 ORDER BY AVG_SAL DESC;





















